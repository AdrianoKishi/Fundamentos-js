// o jS também consegue resolver um problema de diversas maneiras.
// podemos tanto resolver com 1 paradigma só para soluções mais simples
// de problemas mais simples. Teremos uma curva de aprendizagem mais simples
// mais fácil de aprender.

// - Por que se chama JS?

// o jS não é JAVA. É bem diferente a forma como se coda em jS e em JAVA.
// o nome é parecido mas não é a mesma coisa. O jS é bem mais simples e o JAVA
// mais complexo. O ECMAScript é uma especificação de linguagem, ela define os 
// padrões para uma linguagem de programação, enquanto o jS é a 
// implementação desses padrões.

// - O que é ES6 / ES2015

// O ES6 é uma versão que foi implementada ao jS que saiu em 2015, essa versão é bem
// importante pois a partir desse ano começamos a possuir atualizações anuais.
// Foi a versão mais famosa do jS pois trouxe muitas funcionalidades importantes.
// Como por exemplo foi implementada a let e const, variáveis que foram trazidas
// para corrigir bugs e erros que existiam anteriormente.

// - Linguagem interpretada

// Temos 2 tipos de linguagens: Interpretadas e as Compiladas.
// A linguagem interpretada escrevemos nosso código e a partir de um programa
// conseguimos reproduzir o que estará escrito nessa linha instantaneamente
// A linguagem compilada vai passar por um programa chamado: Compilador que vai 
// transformar nosso código em linguagem de máquina, que é um código que nosso
// computador entende. 
// Linguagens compiladas rodam mais rápido, mas precisam de um intermediador.
// Linguagens interpretadas temos mais vantagem em saber o que está sendo 
// interpretado por um programa linha por linha.

